const tabs = document.querySelectorAll(".purpose-tab");
const panels = document.querySelectorAll(".purpose-panel");
const mediaGrid = document.querySelector("#media-grid");
const menuToggle = document.querySelector(".menu-toggle");
const mobileMenu = document.querySelector("#mobile-menu");
let lightbox = null;
let lightboxZoom = 1;

const createText = (tag, text, className) => {
  const element = document.createElement(tag);
  if (className) {
    element.className = className;
  }
  element.textContent = text;
  return element;
};

const closeMobileMenu = () => {
  if (!menuToggle || !mobileMenu) return;
  menuToggle.classList.remove("is-open");
  mobileMenu.classList.remove("is-open");
  menuToggle.setAttribute("aria-expanded", "false");
};

const setLightboxZoom = (zoom) => {
  if (!lightbox) return;
  lightboxZoom = Math.min(Math.max(zoom, 1), 3);
  const image = lightbox.querySelector(".lightbox-image");
  const zoomValue = lightbox.querySelector(".lightbox-zoom-value");
  const imageStage = lightbox.querySelector(".lightbox-image-stage");
  image.style.transform = `scale(${lightboxZoom})`;
  image.style.cursor = lightboxZoom > 1 ? "move" : "zoom-in";
  imageStage.classList.toggle("is-zoomed", lightboxZoom > 1);
  zoomValue.textContent = `${Math.round(lightboxZoom * 100)}%`;
};

const closeLightbox = () => {
  if (!lightbox) return;
  lightbox.classList.remove("is-open");
  lightbox.setAttribute("aria-hidden", "true");
  document.body.classList.remove("has-lightbox");
  setLightboxZoom(1);
};

const openLightbox = (item) => {
  if (!lightbox) return;
  const image = lightbox.querySelector(".lightbox-image");
  const title = lightbox.querySelector(".lightbox-title");
  const description = lightbox.querySelector(".lightbox-description");

  image.src = item.src;
  image.alt = item.alt || item.title;
  title.textContent = item.title;
  description.textContent = item.description;
  lightbox.classList.add("is-open");
  lightbox.setAttribute("aria-hidden", "false");
  document.body.classList.add("has-lightbox");
  setLightboxZoom(1);
};

const createLightbox = () => {
  const overlay = document.createElement("div");
  overlay.className = "lightbox";
  overlay.setAttribute("aria-hidden", "true");
  overlay.setAttribute("role", "dialog");
  overlay.setAttribute("aria-modal", "true");

  const panel = document.createElement("div");
  panel.className = "lightbox-panel";

  const closeButton = document.createElement("button");
  closeButton.type = "button";
  closeButton.className = "lightbox-close";
  closeButton.setAttribute("aria-label", "Cerrar imagen ampliada");
  closeButton.textContent = "x";

  const toolbar = document.createElement("div");
  toolbar.className = "lightbox-toolbar";

  const zoomOutButton = document.createElement("button");
  zoomOutButton.type = "button";
  zoomOutButton.className = "lightbox-tool";
  zoomOutButton.setAttribute("aria-label", "Alejar imagen");
  zoomOutButton.textContent = "-";

  const zoomValue = document.createElement("span");
  zoomValue.className = "lightbox-zoom-value";
  zoomValue.textContent = "100%";

  const zoomInButton = document.createElement("button");
  zoomInButton.type = "button";
  zoomInButton.className = "lightbox-tool";
  zoomInButton.setAttribute("aria-label", "Acercar imagen");
  zoomInButton.textContent = "+";

  const resetButton = document.createElement("button");
  resetButton.type = "button";
  resetButton.className = "lightbox-tool lightbox-reset";
  resetButton.setAttribute("aria-label", "Restablecer zoom");
  resetButton.textContent = "100%";

  toolbar.append(zoomOutButton, zoomValue, zoomInButton, resetButton);

  const imageStage = document.createElement("div");
  imageStage.className = "lightbox-image-stage";

  const image = document.createElement("img");
  image.className = "lightbox-image";
  image.alt = "";
  imageStage.append(image);

  const copy = document.createElement("div");
  copy.className = "lightbox-copy";
  copy.append(createText("h3", "", "lightbox-title"), createText("p", "", "lightbox-description"));

  panel.append(closeButton, toolbar, imageStage, copy);
  overlay.append(panel);
  document.body.append(overlay);

  overlay.addEventListener("click", (event) => {
    if (event.target === overlay) {
      closeLightbox();
    }
  });

  image.addEventListener("click", () => {
    setLightboxZoom(lightboxZoom === 1 ? 1.8 : 1);
  });

  imageStage.addEventListener("wheel", (event) => {
    if (!overlay.classList.contains("is-open")) return;
    event.preventDefault();
    const step = event.deltaY < 0 ? 0.2 : -0.2;
    setLightboxZoom(lightboxZoom + step);
  }, { passive: false });

  closeButton.addEventListener("click", closeLightbox);
  zoomOutButton.addEventListener("click", () => setLightboxZoom(lightboxZoom - 0.25));
  zoomInButton.addEventListener("click", () => setLightboxZoom(lightboxZoom + 0.25));
  resetButton.addEventListener("click", () => setLightboxZoom(1));

  lightbox = overlay;
};

const createMediaElement = (item) => {
  const frame = document.createElement("div");
  frame.className = "media-frame";

  if (item.type === "video") {
    const video = document.createElement("video");
    video.src = item.src;
    video.controls = true;
    video.preload = "metadata";
    video.playsInline = true;
    frame.append(video);
  } else {
    const button = document.createElement("button");
    button.type = "button";
    button.className = "image-open-button";
    button.setAttribute("aria-label", `Ampliar ${item.title}`);

    const image = document.createElement("img");
    image.src = item.src;
    image.alt = item.alt || item.title;
    image.loading = "lazy";

    button.append(image);
    button.addEventListener("click", () => openLightbox(item));
    frame.append(button);
  }

  return frame;
};

const updateCarouselButtons = (track, prevButton, nextButton) => {
  const maxScroll = track.scrollWidth - track.clientWidth - 2;
  prevButton.disabled = track.scrollLeft <= 2;
  nextButton.disabled = track.scrollLeft >= maxScroll;
};

const renderMediaGroups = () => {
  if (!mediaGrid) return;

  createLightbox();

  mediaGroups.forEach((group) => {
    const section = document.createElement("article");
    section.className = `media-carousel media-carousel--${group.items[0]?.type || "image"} reveal`;

    const header = document.createElement("div");
    header.className = "media-carousel-header";

    const copy = document.createElement("div");
    copy.append(createText("h3", group.title), createText("p", group.description));

    const controls = document.createElement("div");
    controls.className = "carousel-controls";

    const prevButton = document.createElement("button");
    prevButton.type = "button";
    prevButton.className = "carousel-button";
    prevButton.setAttribute("aria-label", `Ver anterior en ${group.title}`);
    prevButton.textContent = "<";

    const nextButton = document.createElement("button");
    nextButton.type = "button";
    nextButton.className = "carousel-button";
    nextButton.setAttribute("aria-label", `Ver siguiente en ${group.title}`);
    nextButton.textContent = ">";

    controls.append(prevButton, nextButton);
    header.append(copy, controls);

    const track = document.createElement("div");
    track.className = "media-carousel-track";
    track.tabIndex = 0;
    track.setAttribute("aria-label", `Carrusel de ${group.title}`);

    group.items.forEach((item) => {
      const card = document.createElement("div");
      card.className = `media-card media-card--${item.type}${item.orientation ? ` media-card--${item.orientation}` : ""}`;
      card.append(createMediaElement(item));

      const itemCopy = document.createElement("div");
      itemCopy.className = "media-copy";
      itemCopy.append(createText("h4", item.title), createText("p", item.description));
      card.append(itemCopy);

      track.append(card);
    });

    const scrollByCard = (direction) => {
      const card = track.querySelector(".media-card");
      const distance = card ? card.getBoundingClientRect().width + 18 : track.clientWidth;
      track.scrollBy({ left: direction * distance, behavior: "smooth" });
    };

    prevButton.addEventListener("click", () => scrollByCard(-1));
    nextButton.addEventListener("click", () => scrollByCard(1));
    track.addEventListener("scroll", () => updateCarouselButtons(track, prevButton, nextButton));
    window.addEventListener("resize", () => updateCarouselButtons(track, prevButton, nextButton));

    section.append(header, track);
    mediaGrid.append(section);
    updateCarouselButtons(track, prevButton, nextButton);
  });
};

renderMediaGroups();

if (menuToggle && mobileMenu) {
  menuToggle.addEventListener("click", () => {
    const isOpen = menuToggle.classList.toggle("is-open");
    mobileMenu.classList.toggle("is-open", isOpen);
    menuToggle.setAttribute("aria-expanded", String(isOpen));
  });

  mobileMenu.querySelectorAll("a").forEach((link) => {
    link.addEventListener("click", closeMobileMenu);
  });
}

document.addEventListener("keydown", (event) => {
  if (event.key === "Escape") {
    closeMobileMenu();
    closeLightbox();
  }
});

tabs.forEach((tab) => {
  tab.addEventListener("click", () => {
    const target = tab.dataset.tab;

    tabs.forEach((item) => {
      const isSelected = item === tab;
      item.classList.toggle("is-active", isSelected);
      item.setAttribute("aria-selected", String(isSelected));
    });

    panels.forEach((panel) => {
      const isSelected = panel.dataset.panel === target;
      panel.classList.toggle("is-active", isSelected);
      panel.hidden = !isSelected;
    });
  });
});

const animatedItems = document.querySelectorAll(
  ".service-card, .media-carousel, .brand-visual, .brand-copy, .purpose-layout, .motto-section, .contact-panel"
);

const revealObserver = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("is-visible");
        revealObserver.unobserve(entry.target);
      }
    });
  },
  { threshold: 0.16 }
);

animatedItems.forEach((item) => {
  item.classList.add("reveal");
  revealObserver.observe(item);
});
